package Objetos;

/**
 *
 * @author kaiop
 */
public class Drogaria {
    
   private String nomeDrogaria;
   private String cnpj;
   private String estado;
   private String cidade;
   private String cep;
   private String email;
   private int codDrogaria;
   private String razaoSocial;

    /**
     * @return the nomeDrogaria
     */
    public String getNomeDrogaria() {
        return nomeDrogaria;
    }

    /**
     * @param nomeDrogaria the nomeDrogaria to set
     */
    public void setNomeDrogaria(String nomeDrogaria) {
        this.nomeDrogaria = nomeDrogaria;
    }

    /**
     * @return the cnpj
     */
    public String getCnpj() {
        return cnpj;
    }

    /**
     * @param cnpj the cnpj to set
     */
    public void setCnpj(String cnpj) {
        this.cnpj = cnpj;
    }

    /**
     * @return the estado
     */
    public String getEstado() {
        return estado;
    }

    /**
     * @param estado the estado to set
     */
    public void setEstado(String estado) {
        this.estado = estado;
    }

    /**
     * @return the cidade
     */
    public String getCidade() {
        return cidade;
    }

    /**
     * @param cidade the cidade to set
     */
    public void setCidade(String cidade) {
        this.cidade = cidade;
    }

    /**
     * @return the cep
     */
    public String getCep() {
        return cep;
    }

    /**
     * @param cep the cep to set
     */
    public void setCep(String cep) {
        this.cep = cep;
    }


    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }



    /**
     * @return the codDrogaria
     */
    public int getCodDrogaria() {
        return codDrogaria;
    }

    /**
     * @param codDrogaria the codDrogaria to set
     */
    public void setCodDrogaria(int codDrogaria) {
        this.codDrogaria = codDrogaria;
    }

    /**
     * @return the razaoSocial
     */
    public String getRazaoSocial() {
        return razaoSocial;
    }

    /**
     * @param razaoSocial the razaoSocial to set
     */
    public void setRazaoSocial(String razaoSocial) {
        this.razaoSocial = razaoSocial;
    }
    
}
